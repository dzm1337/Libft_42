#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*new_str;

	i = 0;
	while (s[i])
		i++;
	new_str = (char *)malloc(sizeof(char) * i);
	if (!new_str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_str[i] = s[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
